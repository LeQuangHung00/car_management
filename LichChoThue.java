    public void SearchCar() {
        Vector cols = new Vector();
        cols.addElement("Số Xe");
        cols.addElement("Tên Xe");
        cols.addElement("Phí Xăng");
        cols.addElement("Phí Quá Km");
        cols.addElement("Đơn Giá ");
        cols.addElement("Mô Tả");
        cols.addElement("Trạng Thái");
        Vector data = new Vector();
        String textSearchCar = txtSearch.getText().trim();
        String sql = "SELECT * FROM xe";
        //System.out.println("ket noi bang");
        try {
            stmt = con.prepareStatement(sql);
            rs = stmt.executeQuery();
            while (rs.next()) {
                Vector car = new Vector();
                car.addElement(rs.getString("SoXe"));
                car.addElement(rs.getString("TenXe"));
                car.addElement(rs.getString("PhiXang"));
                car.addElement(rs.getString("PhiQuaKm"));
                car.addElement(rs.getString("DonGia"));
                car.addElement(rs.getString("MoTaTT"));
                car.addElement(rs.getString("TrangThai"));
                if (car.toString().toUpperCase().contains(textSearchCar.toUpperCase())) {
                    data.add(car);
                }
            }
        } catch (SQLException ex) {
//            Logger.getLogger(LichChoThue.class.getName()).log(Level.SEVERE, null, ex);
        }
        tblCar.setModel(new DefaultTableModel(data, cols));
    }
