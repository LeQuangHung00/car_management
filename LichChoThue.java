    public void showCar() {
        //System.out.println("Tao vector");
        //tao ten cot
        Vector cols = new Vector();
        cols.addElement("STT");
        cols.addElement("Khách hàng");
        cols.addElement("Ngày Đăng ký");
        cols.addElement("Ngày cho thuê");
        cols.addElement("Số ngày thuê");
        cols.addElement("Loại xe");

        //tao vector du lieu
        Vector data = new Vector();

        String sql = "SELECT lichchothue.STT,lichchothue.MaKH,lichchothue.MaLoai,lichchothue.NgayDK,lichchothue.NgayThue,lichchothue.SoNgayThue,khachhang.TenKH, loaixe.tenloai  FROM lichchothue, khachhang, loaixe where khachhang.MaKH = lichchothue.MaKH and lichchothue.MaLoai = loaixe.MaLoai;";
        //System.out.println("ket noi bang");

        try {
            stmt = con.prepareStatement(sql);
            rs = stmt.executeQuery();
            while (rs.next()) {
                Vector car = new Vector();
                car.addElement(rs.getString("lichchothue.STT"));
                car.addElement(rs.getString("khachhang.TenKH"));
                car.addElement(rs.getString("lichchothue.NgayDK"));
                car.addElement(rs.getString("lichchothue.NgayThue"));
                car.addElement(rs.getString("lichchothue.SoNgayThue"));
                car.addElement(rs.getString("loaixe.Tenloai"));
                data.add(car);
            }
        } catch (SQLException ex) {
            Logger.getLogger(LichChoThue.class.getName()).log(Level.SEVERE, null, ex);
        }
        tblCar.setModel(new DefaultTableModel(data, cols));
    }

